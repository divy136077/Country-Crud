export enum RegexEnum {
    name = '^(?! )[A-Za-z -]*(?<! )$',
    phone = '^[0-9]*$',
    amount = '^([0-9]+(\.[0-9]+)?)',
    numeric = '^[0-9]*\.?[0-9]+$',
    alpha_spaces = '^[a-zA-Z ]+$',
    alpha_numeric = '^[A-Z0-9]+$',
    email =
    '^([0-9a-zA-Z]([-_\\.]*[0-9a-zA-Z]+)*)' +
    '@([0-9a-zA-Z]([-_\\.]*[0-9a-zA-Z]+)*)[\\.]([a-zA-Z]{2,9})+$',
    emailLength = '^(?=[^@]{6,}@)([\w\.-]*[a-zA-Z0-9_]@(?=.{3,}\.[^.]*$)[\w\.-]*[a-zA-Z0-9]\.[a-zA-Z][a-zA-Z\.]*[a-zA-Z])$',
    mobile = '^((\\+91-?)|0)?[0-9]{10}$',
    url = '(https?://)?([\\da-z.-]+)\\.([a-z.]{2,6})[/\\w .-]*/?',
    zipcode = '^[0-9]{5}(-[0-9]{4})?$',
    passwordValidation = '^(?!.* )(?=.*?[A-Z])(?=.*?[a-z])(?=.*[!@#$%^&*])(?=.*[0-9]).{8,15}$',
    link = '^(http:\/\/www\.|https:\/\/www\.|http:\/\/|https:\/\/)[a-z0-9]+([\-\.]{1}[a-z0-9]+)*\.[a-z]{2,5}(:[0-9]{1,5})?(\/.*)?$',
    macAddress = '^([0-9A-Fa-f]{2}[:-]?){5}([0-9A-Fa-f]{2})$',
    number = '^[0-9]*$',
    comapnyName = '^[A-Z]([a-zA-Z0-9]|[- @\.#&!])*$',
}
